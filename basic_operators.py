# Addition
def add(x,y):
	return x + y

print("Sum", add(15, 5))


def add(x,y,z):
	return x + y + z

print("Sum", add(15, 5, 10))


# Subtraction
def subtract(x,y):
	return x - y

print("Difference", subtract(15, 5))


def subtract(x,y,z):
	return x - y - z

print("Difference", subtract(100, 20, 5))

# Multiplication
def multiply(x,y):
	return x * y

print("Product", multiply(15,5))


def multiply(x,y,z):
	return x * y * z

print("Product", multiply(8, 2, 5))

# Division
def divide(x,y):
	return x / y

print("Quotient", divide(15,5))


def add_subtract(x,y,z):
	return x + y - z

print("Add Subtract", add_subtract(10, 5, 3))

# Modulus
def modulus(x,y):
	return x % y

print("Remainder", modulus(15,7))

# Exponential
def exponent(x,y):
	return x ** y

print("Exponent", exponent(5,3))

# Floor Division
def flr_divide(x,y):
	return x // y

print("Floor Division", flr_divide(15,10))